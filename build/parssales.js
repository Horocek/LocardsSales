const timeConverter = unixTime => {
    const months = ['01', '02', '03',
        '04', '05', '06',
        '07', '08', '09',
        '10', '11', '12'
    ];
    const date = new Date(unixTime * 1000);
    return `${date.getDate()}-${months[date.getMonth()]}-${date.getFullYear()} / ${date.getHours()}:${date.getMinutes()}:${date.getSeconds()}`;
}
